            <span class="h5 mt-4 font-italic">Tisztelt Egyetemi Polgár!</span>

            <p style="text-align: justify;">Az Eötvös József Collegium igazgatójaként gratulálok sikeres egyetemi felvételijéhez, és köszöntöm az egyetemi polgárok között. Tanulmányaihoz sok örömet és sikereket kívánok!</p>

            <p style="text-align: justify;">Kérem, jelentkezzen az ELTE Eötvös József Collegium felvételijére! Sokéves tapasztalatunk, hogy az Eötvös Collegium hírneve olykor inkább elijeszti, semmint felbátorítja a vidéki hallgatókat. A Collegium nem bevehetetlen fellegvár, ahol publikációk, „szakmai” teljesítmények, gimnáziumi és egyetemi dolgozatok híján nem is érdemes megjelenni. Sikeres felvételije egyértelmű bizonysága annak, hogy helye van az Eötvös Collegiumba jelentkezők között. Szívesen látjuk! A Collegium hivatása alapításától fogva az Önhöz hasonló hallgatók támogatása. A collegiumi tagságra az ELTE Bölcsészettudományi, Informatikai, Társadalomtudományi és Természettudományi Karára, valamint Gazdálkodástudományi Intézetébe felvett nappali tagozatos hallgatók pályázhatnak, illetve kivételes esetben olyanok, akik a Collegium valamelyik műhelyéhez kapcsolódó minort végeznek. Felvételizni a képzések teljes időtartama alatt lehetséges. 2015-től különösen fontos számunkra, hogy a collegiumi tagságot az ELTE valamennyi karán tanuló tanárszakos hallgatók is minél nagyobb számban elnyerjék. A Collegium vidéki és budapesti hallgatók számára egyaránt nyitott. A budapesti diákok bejáró státuszra tarthatnak igényt, amely teljes jogú collegiumi tagságot biztosít.</p>

            <p>A pályázat beküldési határideje: <strong>2020. augusztus <del>12.</del> <strong class="text-danger">15.</strong> 24.00 óra</strong></p>

            <p>A pályázatnak a következőt kell tartalmaznia:</p>

            <ul>
                <li>Felvételi határozat</li>
                <li>Önéletrajz (hagyományos, leíró jellegű)</li>
                <li>Érettségi bizonyítvány</li>
                <li>Az Ön által legjobbnak ítélt saját munkája (csak a Filozófia műhelybe jelentkezés esetén)</li>
            </ul>

            <p>Csatolható továbbá:</p>

            <ul>
                <li>Nyelvvizsga bizonyítványok</li>
                <li>Szakmai eredmények dokumentumai másolatban (pl. tanulmányi versenyek igazolása, oklevelek stb. pdf/jpeg formátumban, szkennelve)</li>
                <li>Az Ön korábbi munkáját jól ismerő szaktanár ajánlása</li>
            </ul>

            <p style="text-align: justify;">A szaktanárok előzetes véleményezését követően a collegiumi felvételire behívottak névsora – a felvételi űrlapon való egyéni hozzájárulás alapján – augusztus 18-án 10 órától tekinthető meg a Collegium honlapján. A felvételi idén rendhagyó módon online, a Microsoft Teams rendszeren keresztül kerül lebonyolításra. A felvételi szóbeli beszélgetésekből áll, melyet a műhelyek saját hatáskörben írásbeli résszel egészíthetnek ki. A felvételi kizárólagos időpontja: augusztus 24-25. A kétnapos eseményen a felvételizőt műhelyenként egy-egy oktatói és hallgatói bizottság fogja kérdezni, majd a sikeresen továbbjutó bentlakó jelentkezőknek az igazgatóval kell beszélgetniük.</p>

            <p>Technikai információk:</p>

            <p style="text-align: justify;">A jelentkezés során a pályázóknak meg kell adniuk az ELTE-s azonosítójukat (más néven IIG-s, Caesar-os), mellyel a Teams rendszert használhatják (ez minden hallgatónak biztosított). Az azonosítót az alábbi oldalon történő bejelentkezéssel igényelhetik a felvételi értesítőt követő napokban az egyetem részéről e-mailben kiküldött Neptun azonosítóval és jelszóval: <a href="https://ugykezelo.elte.hu/">https://ugykezelo.elte.hu/</a> A behívott hallgatókat az itt megadott azonosítóval fogjuk felvenni a Teams-en létrehozott Felvételi csoportba, ahol a felvételit fogjuk lebonyolítani. Itt nincs teendőjük a felvételi kezdetéig (augusztus 24.), annak pontos menetéről a behívottakat e-mailben értesítjük. A felvételi során a jelentkezőknek webkamerát kell használniuk, annak meglétét a felvételiig kérjük biztosítsák.</p>

